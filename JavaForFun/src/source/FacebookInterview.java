package source;


public class FacebookInterview {
	public static void main(String[] args) {

		for (int i = 1; i < 100; i++) {
			if (i % 15 == 0) {
				System.out.println("LOLWAT");

			} else if (i % 5 == 0) {
				System.out.println("LOL");
			} else if (i % 3 == 0) {
				System.out.println("WAT");

			} else {
				System.out.println(i);
			}

		}

	}
}
